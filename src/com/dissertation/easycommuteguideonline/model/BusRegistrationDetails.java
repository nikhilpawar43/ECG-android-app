package com.dissertation.easycommuteguideonline.model;

public class BusRegistrationDetails {
	
	private String boardingPointName;
	private String morningBusRouteno;
	private String morningBusRoutename;
	private String eveningBusRouteno;
	private String eveningBusRoutename;
	
	public String getBoardingPointName() {
		return boardingPointName;
	}
	public void setBoardingPointName(String boardingPointName) {
		this.boardingPointName = boardingPointName;
	}
	
	public String getMorningBusRouteno() {
		return morningBusRouteno;
	}
	public void setMorningBusRouteno(String morningBusRouteno) {
		this.morningBusRouteno = morningBusRouteno;
	}
	
	public String getMorningBusRoutename() {
		return morningBusRoutename;
	}
	public void setMorningBusRoutename(String morningBusRoutename) {
		this.morningBusRoutename = morningBusRoutename;
	}
	
	public String getEveningBusRouteno() {
		return eveningBusRouteno;
	}
	public void setEveningBusRouteno(String eveningBusRouteno) {
		this.eveningBusRouteno = eveningBusRouteno;
	}
	
	public String getEveningBusRoutename() {
		return eveningBusRoutename;
	}
	public void setEveningBusRoutename(String eveningBusRoutename) {
		this.eveningBusRoutename = eveningBusRoutename;
	}
	
	

}
